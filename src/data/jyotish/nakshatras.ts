/**
 * Отримати накшатру за градусом Місяця (0-360)
 */
export function getNakshatraByDegree(moonDegree: number): Nakshatra {
  const normalizedDegree = ((moonDegree % 360) + 360) % 360;
  const nakshatraIndex = Math.floor(normalizedDegree / 13.333333);
  return NAKSHATRAS[nakshatraIndex] || NAKSHATRAS[0];
}

/**
 * Отримати накшатру за ID (1-27)
 */
export function getNakshatraById(id: number): Nakshatra | undefined {
  return NAKSHATRAS.find(n => n.id === id);
}

/**
 * Отримати всі накшатри, якими володіє певна планета
 */
export function getNakshatrasByRuler(planet: GrahaPlanet): Nakshatra[] {
  return NAKSHATRAS.filter(n => n.ruler_planet === planet);
}

/**
 * Отримати прогрес всередині накшатри (0-100%)
 */
export function getNakshatraProgress(moonDegree: number): number {
  const normalizedDegree = ((moonDegree % 360) + 360) % 360;
  const nakshatraSize = 13.333333;
  const positionInNakshatra = normalizedDegree % nakshatraSize;
  return (positionInNakshatra / nakshatraSize) * 100;
}

/**
 * Отримати паду (чверть) накшатри (1-4)
 */
export function getNakshatraPada(moonDegree: number): number {
  const normalizedDegree = ((moonDegree % 360) + 360) % 360;
  const nakshatraSize = 13.333333;
  const padaSize = nakshatraSize / 4;
  const positionInNakshatra = normalizedDegree % nakshatraSize;
  return Math.floor(positionInNakshatra / padaSize) + 1;
}

