/**
 * Отримати раші за градусом (0-360)
 */
export function getRashiByDegree(degree: number): Rashi {
  const normalizedDegree = ((degree % 360) + 360) % 360;
  const rashiIndex = Math.floor(normalizedDegree / 30);
  return RASHIS[rashiIndex] || RASHIS[0];
}

/**
 * Отримати раші за ID (1-12)
 */
export function getRashiById(id: number): Rashi | undefined {
  return RASHIS.find(r => r.id === id);
}

/**
 * Отримати раші, якими володіє планета
 */
export function getRashisByRuler(planet: GrahaPlanet): Rashi[] {
  return RASHIS.filter(r => r.ruler_planet === planet);
}

/**
 * Отримати прогрес всередині раші (0-100%)
 */
export function getRashiProgress(degree: number): number {
  const normalizedDegree = ((degree % 360) + 360) % 360;
  const positionInRashi = normalizedDegree % 30;
  return (positionInRashi / 30) * 100;
}

/**
 * Отримати раші, де планета екзальтована
 */
export function getExaltationRashi(planet: GrahaPlanet): Rashi | undefined {
  return RASHIS.find(r => r.exalted_planet === planet);
}

/**
 * Отримати раші, де планета в падінні
 */
export function getDebilitationRashi(planet: GrahaPlanet): Rashi | undefined {
  return RASHIS.find(r => r.debilitated_planet === planet);
}

export default RASHIS;
